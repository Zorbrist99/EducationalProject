def primer_6():
    summa = 0
    for i in range(1, 101):
        if i % 2 != 0:
            summa += i
    print(summa)


"""
Пользователь вводит число. Выведи таблицу умножения для него от 1 до 10.
"""


def primer_7():
    a = int(input("Введи число: "))
    for i in range(1, 11):
        mnog = a * i
        print(f'{a} * {i} = {mnog}')


"""
Пользователь вводит строку. Посчитай, сколько в ней гласных букв (а, е, ё, и, о, у, ы, э, ю, я, учитывая и строчные, и прописные).
"""


def primer_8():
    a = input("Введи текст на русском: ")
    glass = 0
    lower = 'аеёиоуыэюя'

    for b in a:
        if b.lower() in lower:
            glass += 1

    print(glass)


"""
Пользователь вводит ширину и высоту. Построй прямоугольник из # вида
"""


def primer_9():
    h = int(input('Введи высоту: '))
    d = int(input('Введи длину: '))

    # Таким образом можно сократить код. Отсчет будет с 0
    for i in range(h):
        print('# ' * d)


"""
Пользователь вводит число n. Выведи n! (произведение всех чисел от 1 до n).
"""


def primer_10():
    a = int(input("Введите число: "))
    proiz = 1
    for i in range(1, a + 1):
        proiz *= i
    print(proiz)
