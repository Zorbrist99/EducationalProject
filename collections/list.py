"""
Создай список из 5 чисел.
Выведи:

Первые 3 элемента с помощью среза

Последние 2 элемента с помощью среза
"""


def primer_2():
    a = [1, 2, 3, 4, 5]

    print(a[:3])
    print(a[-2:])


"""
Выведи срез, который включает только 20, 30 и 40.

"""


def primer_3():
    a = [10, 20, 30, 40, 50, 60]

    print(a[1:4])


"""
Выведи срез, который берёт каждый второй элемент, начиная с первого.
"""


def primer_4():
    b = ['a', 'b', 'c', 'd', 'e', 'f']

    print(b[1::2])


"""
Выведи элементы в обратном порядке, используя только срез.
"""


def primer_5():
    c = [1, 2, 3, 4, 5, 6, 7, 8]
    # c.reverse()
