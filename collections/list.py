"""
✅ Задание 1
Создай список из чисел [1, 2, 3].
Добавь в него число 4 с помощью append() и выведи результат.
"""


def primer_6():
    a = [1, 2, 3]
    a.append(4)
    print(a)


"""
✅ Задание 2
Создай список из элементов ['яблоко', 'банан', 'вишня', 'банан'].
Удалите первый элемент 'банан' методом remove() и выведи список.
"""


def primer_7():
    a = ['яблоко', 'банан', 'вишня', 'банан']
    a.remove('банан')
    print(a)


"""
✅ Задание 3
Создай список [10, 20, 30].
Удалите последний элемент с помощью pop() и выведите:

сначала удалённый элемент,

затем изменённый список.
"""


def primer_8():
    a = [10, 20, 30]
    b = a.pop()
    print(b)
    print(a)


"""
✅ Задание 4
Есть список [5, 2, 9, 1].
Отсортируй его по возрастанию методом sort() и выведи результат.
"""

def primer_9():
    a = [5, 2, 9, 1]
    a.sort()
    print(a)

"""
✅ Задание 5
Есть список ['зима', 'весна', 'лето', 'осень'].
Разверни его в обратном порядке методом reverse() и выведи результат.
"""

def primer_10():
    a = ['зима', 'весна', 'лето', 'осень']
    a.reverse()
    print(a)
