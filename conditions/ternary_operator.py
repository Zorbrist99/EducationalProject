"""


def minimum_of_two_numbers():
    one = float(input('–í–≤–µ–¥–∏ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ: '))
    two = float(input('–í–≤–µ–¥–∏ –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ: '))

    min_value = one if one < two else two
    print(f"–ú–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: {min_value}")


minimum_of_two_numbers()

"""
