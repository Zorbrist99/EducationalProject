"""
Выведи числа от 1 до 10 включительно с помощью for.
"""


def primer_1():
    for i in range(1, 11):
        print(i)


"""
Выведи квадраты чисел от 1 до 5.
Подсказка: квадрат числа — это i ** 2.
"""


def primer_2():
    for i in range(1, 6):
        print(i ** 2)


"""
Выведи все чётные числа от 2 до 20 включительно.
"""


def primer_3():
    for i in range(2, 21):
        if i % 2 == 0:
            print(i)


"""
Пользователь вводит 5 чисел. Выведи их сумму.
"""


def primer_4():
    summa = 0
    for i in range(1, 6):
        a = float(input("Введите число: "))
        summa += a
    print(summa)


