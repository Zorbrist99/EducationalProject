"""
Создай список из трёх любых животных. Затем выведи:

Первый элемент

Последний элемент (с помощью отрицательного индекса)
"""


def primer_1():
    a = ['Собака', 'Кошка', 'Лошадь']

    print(a[0])
    print(a[-1])


