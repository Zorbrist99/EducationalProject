def comparing_three_numbers():
    one = float(input('–í–≤–µ–¥–∏ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ: '))
    two = float(input('–í–≤–µ–¥–∏ –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ: '))
    three = float(input('–í–≤–µ–¥–∏ —Ç—Ä–µ—Ç—å–µ —á–∏—Å–ª–æ: '))

    max_value = one if two < one > three \
        else two if one < two > three \
        else three
    print(f'–ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ä–∞–≤–Ω–æ: {max_value}')


comparing_three_numbers()

